'use client';

import { useTheme } from 'next-themes';

/**
 * Light/dark theme toggle.
 *
 * Both icons are always rendered and shown/hidden purely with the `dark:`
 * variant (driven by the `.dark` class next-themes sets on <html> before
 * hydration). That avoids a `mounted` state / hydration flash and keeps the
 * correct icon visible from first paint.
 */
export function ThemeToggle() {
  const { resolvedTheme, setTheme } = useTheme();

  return (
    <button
      type="button"
      onClick={() => setTheme(resolvedTheme === 'dark' ? 'light' : 'dark')}
      className="w-[40px] h-[40px] flex items-center justify-center rounded-lg border border-border bg-card text-foreground/70 active:bg-card/60 transition-colors shrink-0"
      aria-label="Toggle light or dark theme"
    >
      {/* Sun — visible in light mode */}
      <svg
        className="block dark:hidden"
        xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24"
        fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"
      >
        <circle cx="12" cy="12" r="5" />
        <line x1="12" y1="1" x2="12" y2="3" />
        <line x1="12" y1="21" x2="12" y2="23" />
        <line x1="4.22" y1="4.22" x2="5.64" y2="5.64" />
        <line x1="18.36" y1="18.36" x2="19.78" y2="19.78" />
        <line x1="1" y1="12" x2="3" y2="12" />
        <line x1="21" y1="12" x2="23" y2="12" />
        <line x1="4.22" y1="19.78" x2="5.64" y2="18.36" />
        <line x1="18.36" y1="5.64" x2="19.78" y2="4.22" />
      </svg>
      {/* Moon — visible in dark mode */}
      <svg
        className="hidden dark:block"
        xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24"
        fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"
      >
        <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z" />
      </svg>
    </button>
  );
}
